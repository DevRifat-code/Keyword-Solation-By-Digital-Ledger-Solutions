function parseJSONRobustly(text: string): any {
  let cleaned = text.trim();

  // If wrapped in markdown or has intro/outro text, extract the JSON object
  const firstBrace = cleaned.indexOf("{");
  const lastBrace = cleaned.lastIndexOf("}");
  if (firstBrace !== -1 && lastBrace !== -1 && lastBrace > firstBrace) {
    cleaned = cleaned.substring(firstBrace, lastBrace + 1);
  } else if (cleaned.startsWith("```")) {
    cleaned = cleaned.replace(/^```json\s*/i, "").replace(/```$/, "").trim();
  }

  // Sanitize unescaped newlines and unescaped double-quotes inside string values
  let sanitized = "";
  let insideString = false;
  let escaped = false;
  for (let i = 0; i < cleaned.length; i++) {
    const char = cleaned[i];
    if (insideString) {
      if (escaped) {
        sanitized += char;
        escaped = false;
      } else if (char === '\\') {
        sanitized += char;
        escaped = true;
      } else if (char === '"') {
        // Check if it's the ending quote of a key or a string value.
        // It is followed by optional whitespace and then a separator (colon, comma, brace, bracket).
        let nextNonWS = "";
        for (let j = i + 1; j < cleaned.length; j++) {
          if (!/\s/.test(cleaned[j])) {
            nextNonWS = cleaned[j];
            break;
          }
        }
        if (nextNonWS === "," || nextNonWS === "}" || nextNonWS === "]" || nextNonWS === ":") {
          insideString = false;
          sanitized += char;
        } else {
          // Unescaped quote inside string value! Escape it.
          sanitized += '\\"';
        }
      } else if (char === '\n') {
        sanitized += '\\n';
      } else if (char === '\r') {
        sanitized += '\\r';
      } else {
        sanitized += char;
      }
    } else {
      sanitized += char;
      if (char === '"') {
        insideString = true;
        escaped = false;
      }
    }
  }

  try {
    return JSON.parse(sanitized);
  } catch (err: any) {
    console.warn("Standard JSON parse failed, attempting robust cleanup with jsonrepair...", err.message);
    try {
      const repaired = jsonrepair(sanitized);
      return JSON.parse(repaired);
    } catch (repairErr: any) {
      console.warn("jsonrepair failed, attempting manual regex fallback...", repairErr.message);
      // Fallback: remove trailing commas in arrays/objects e.g. [1, 2,] or {a: 1,}
      let fallbackCleaned = sanitized.replace(/,\s*([\]}])/g, "$1");
      // Remove non-standard control characters
      fallbackCleaned = fallbackCleaned.replace(/\t/g, " ");

      try {
        return JSON.parse(fallbackCleaned);
      } catch (err2: any) {
        console.error("Robust JSON cleanup failed. Raw text preview:", text.substring(0, 1000));
        throw new Error(`Invalid response format generated by AI: ${err2.message}`);
      }
    }
  }
}

